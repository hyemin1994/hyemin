int n = 25;
int vcolor[26];
int times = 0;

bool promising(int i){
    int j;
    bool pro;
    
    pro = true;
    j = 1;
    while(j<i && pro){
        if(W[i][j] && vcolor[i] == vcolor[j])
            pro = false;
        j++;
    }
    return pro;
}
void m_coloring(int i, int m){
    int color;
    if(promising(i)){
        if(i == n){
            times++;
            //for(int k = 0; k <25; k++)
                //cout<<vcolor[k]<<" ";
        }
        else{
            for(color = 1; color <= m; color++){
                vcolor[i+1] = color;
                m_coloring(i+1,m);
            }
        }
    }
}

